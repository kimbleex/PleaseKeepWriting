export function getPublishedAnnouncements(): Announcement[] {
  return [...announcements].sort((a, b) => {
    if (Boolean(a.pinned) !== Boolean(b.pinned)) return a.pinned ? -1 : 1;
    if (a.publishedAt === b.publishedAt) return b.id.localeCompare(a.id);
    return a.publishedAt < b.publishedAt ? 1 : -1;
  });
}

export function getAnnouncementIds(): string[] {
  return getPublishedAnnouncements().map((announcement) => announcement.id);
}

export function findAnnouncementById(id: string): Announcement | undefined {
  return announcements.find((announcement) => announcement.id === id);
}
